def soma(a,b):
    print("soma:",a+b)

def sub(a,b):
    print("subtração:",a-b)

def mult(a,b):
    print("multiplicação:",a*b)

def div(a,b):
    if b!=0:
        print("divisão:",a/b)
    elif b==0:
        print("não e possivel dividir por zero!")


def pot(a,b):
    print("potenciacão:",a ** b)

def rad(a,b):
    print("radiaciação:",a // b)

